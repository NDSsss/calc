package com.example.nds.calc;

public class Funkcii {
    public double Sum(double a, double b)
    {
        return a+b;
    }
    public double Sub(double a, double b)
    {
        return a-b;
    }
    public double Mul(double a, double b)
    {
        return a*b;
    }
    public double Div(double a, double b)
    {
        return a/b;
    }
}
